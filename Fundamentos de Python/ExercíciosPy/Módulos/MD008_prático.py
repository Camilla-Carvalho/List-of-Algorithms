import math
num = int(input('Digite um numero: '))
raiz = math.sqrt(num)
print(f'A raiz de {num} será: {raiz, math.floor(raiz)}(arredondando)')
#ceil --> comando para arredondar resultados matematicos para cima
#floor--> função contrária do ceil, assim.. arredondando para baixo
